    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel RecordTable =(DefaultTableModel)jTable1.getModel();
        int SelectedRows = jTable1.getSelectedRow();
        
        try{
            
            int id = Integer.parseInt(RecordTable.getValueAt(SelectedRows,0).toString());
            
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection(dataConn,username, password);
            pst = conn.prepareStatement("update teacher set staffID=?,firstname =?,lastname =?,username =?,address =?,class=?,age=? where id =?");
            
            pst.setString(1,txtstudID.getText());
            pst.setString(2,txtFname.getText());
            pst.setString(3,txtLname.getText());
            pst.setString(4,txtUname.getText());
            pst.setString(5,txtAddy.getText());
            pst.setString(6,(String)txtClass.getSelectedItem());
            pst.setString(7,txtAge.getText());
            pst.setString(8,(String)txtGender.getSelectedItem());
           
            pst.setInt(9, id);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Staff added!");
            updateDB();
        }
        catch (ClassNotFoundException ex) {
            java.util.logging.Logger.getLogger(AdminAct.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            System.err.println(ex);
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed
