    public void updateDB(){
        
        int q, i;
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection(dataConn,username, password);
            pst = conn.prepareStatement("select * from teacher");
            
            rs = pst.executeQuery();
            ResultSetMetaData StData = rs.getMetaData();
            
            q = StData.getColumnCount();
            DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
            RecordTable.setRowCount(0);
            
            
            while(rs.next()){
                
                Vector columnData = new Vector();
                
                for (i=1; i<=q; i++){
                    columnData.add(rs.getString("id"));
                    columnData.add(rs.getString("staffID"));
                    columnData.add(rs.getString("firstname"));
                    columnData.add(rs.getString("lastname"));
                    columnData.add(rs.getString("username"));
                    columnData.add(rs.getString("address"));
                    columnData.add(rs.getString("class"));
                    columnData.add(rs.getString("age"));
                    columnData.add(rs.getString("gender"));
                }
                    RecordTable.addRow(columnData);
                
                
            }
            
            
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        } 
    }
    
    
    
    
    
    
    
